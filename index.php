<script>
    // Generate Stars Dynamically
    const starsContainer = document.querySelector(".stars");

    function createStar() {
        let star = document.createElement("div");
        star.className = "star";
        star.style.left = Math.random() * 100 + "vw";
        star.style.top = Math.random() * 100 + "vh";
        star.style.animationDuration = Math.random() * 3 + 2 + "s";
        starsContainer.appendChild(star);

        setTimeout(() => { star.remove(); }, 5000);
    }

    for (let i = 0; i < 50; i++) {  // Generate 50 stars
        createStar();
    }

    setInterval(createStar, 500);

    // Generate Raindrops Dynamically
    const rainContainer = document.querySelector(".rain");

    function createRaindrop() {
        let raindrop = document.createElement("span");
        raindrop.style.left = Math.random() * 100 + "vw";
        raindrop.style.animationDuration = Math.random() * 2 + 1 + "s";
        rainContainer.appendChild(raindrop);

        setTimeout(() => { raindrop.remove(); }, 2000);
    }

    setInterval(createRaindrop, 100);

    // Create an intersection observer to watch the sections
    const sections = document.querySelectorAll('.section');

    const observerOptions = {
        root: null, // Observer will check the viewport
        rootMargin: '0px',
        threshold: 0.5 // Trigger when 50% of the section is in view
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                // Add the visible class when the section is in view
                entry.target.classList.add('visible');
            } else {
                // Optionally, remove the visible class if the section goes out of view
                entry.target.classList.remove('visible');
            }
        });
    }, observerOptions);

    // Observe each section
    sections.forEach(section => {
        observer.observe(section);
    });
</script>
